function toLeet(str) {

  var trans = 
    {'a':['4', '@'],'b':['|3', '8'],'d':['|)', 'o|'],'e':['3'],'f':['|='],
     'g':['9', '6'],'h':['|-|', ']-[', '}-{', '(-)', ')-(', '#'],'i':['1', '!', ']['],
     'j':['_|'],'k':['|<', '|{'],'l':['|_'],'n':['|\\|'],'o':['0'],'p':['|2', '|D'],
     'q':['(,)'],'r':['|Z', '|?'],'s':['5', '$'],'t':['+', '7'],'v':['|/', '\\/'],
     'w':['\\^/', '//'],'x':['><', '}{'],'y':['`/'],'z':['(\\)']
     }
     
  var word = ""
  for (var j of str){
    var call = trans[j]
    call != undefined 
            ?
            (
            word += call[0],
            trans[j] = call.slice(1,call.length).concat([call[0]])
            )
            :
            word += j
  }
  return word  
}
